# HTML

# HTML, bir programlama dili olarak tanımlanamaz. Zira HTML kodlarıyla kendi başına çalışan bir program yazılamaz. Ancak bu dili yorumlayabilen programlar aracılığıyla çalışabilen programlar yazılabilir. Programlama dili denilememesinin nedeni tam olarak budur. Temel görevi yazı, görüntü, video gibi değişik verileri ve bunları içeren sayfaları birbirine basitçe bağlamak, buna ek olarak söz konusu sayfaların web tarayıcısı yazılımları tarafından düzgün olarak görüntülenmesi için gerekli kuralları belirlemektir. 

# HTML Locators (Konumlandırıcı),:

# HTML Locators, bir web sayfasındaki belirli bir HTML öğesini (element) bulmak için kullanılan yöntemlerdir. Bu öğeler, bir web sayfasındaki metin, butonlar, bağlantılar, formlar, resimler, tablolar vb. gibi çeşitli bileşenleri içerebilir.

# HTML Locators, web sayfalarında otomatik test yapmak için kullanılan araçlarda (örneğin Selenium gibi) kullanılır. Bu araçlar, bir web sayfasını otomatik olarak tarar ve belirli HTML öğelerini bulmak ve etkileşimde bulunmak için HTML Locators kullanır.

# HTML Locators, birkaç farklı türde olabilir. Bunlar arasında ID, class, name, tagname, link text ve partial link text gibi türler yer alabilir. Bu Locator türlerinin her biri, HTML öğelerini farklı şekillerde tanımlamak için kullanılır. Örneğin, ID Locator, bir HTML öğesini benzersiz bir kimlik numarasıyla tanımlamak için kullanılırken, class Locator, bir HTML öğesini sınıf adıyla tanımlamak için kullanılır.

# Selenium'da aksiyonlar:
# back() : Önceki sayfaya dönmek için kullanılır.
# build() :  çalıştırılmak üzere inşa eder
# clear() : Metin kutusundaki mevcut metni silmek için kullanılır.
# click() : Farenin geçerli konumuna tıklamak için kullanılır ya da istenilen alana sol tıklar
# clickAndHold(): Fareyi bulunduğu noktada basılı tutmayı sağlar.
# contextClick() : Fare üzerinde sağ tıklama gerçekleştirir.
# doubleClick():  Öğe üzerinde çift tıklama gerçekleştirir.
# dragAndDrop() : İstenilen nesneyi istenilen yere sürükleyip bırakır
# forward() : Sonraki sayfaya gitmek için kullanılır.
# keyDown() : Sonraki sayfaya gitmek için kullanılır.
# keyUp() :  Basılı tuşu serbest bırakmadan yeni tuşa basmayı gerçekleştirir.
# Maximize_window():  Tam ekran yapmak için kullanılır.
# moveToElement() :  Fare işaretçisini öğenin merkezine kaydırır.
# pause() : Testi duraklatır.
# refresh(): Sayfayı yenilemek için kullanılır.
# scroll (): Aşağı veya yukarı kaydırmak için kullanılır.
# scrollByAmount(): İstenilen miktarda sayfanın kaydırılmasını sağlar
# scrollToElement(): aranılan nesneye kadar kaydırılmasını sağlar
# Select_by_visible_text(): Görünür metine tıklamak için kullanılır.
# sendKeys(): istenilen yere istenilen karakterlerin yazılmasını sağlar
# submit ():Teslim etmek/göndermek için kullanılır.